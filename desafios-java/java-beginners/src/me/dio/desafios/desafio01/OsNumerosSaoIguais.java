package me.dio.desafios.desafio01;

import java.util.Scanner;

public class OsNumerosSaoIguais {

	public static void main(String[] args) {
		Scanner teclado = new Scanner(System.in);
		int numero1 = teclado.nextInt();
		int numero2 = teclado.nextInt();

		System.out.println(verificarIgualdade(numero1, numero2));

		teclado.close();
	}

	public static String verificarIgualdade(int a, int b) {
		if (a == b) {
			return "Sao iguais!";
		}
		return "Nao sao iguais!";
	}
}


